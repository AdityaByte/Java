// Comments in Java 
// Java ignore the lines starting with these symbols // and /* */

public class CommentsInJava {
    public static void main(String[] args) {
        System.out.println("Comments in Java - ");
        System.out.println("One liner comment in java starts with double forward slash - //");
        // like this
        System.out.println("Multi line comment starts with /* and ends with */");
        /* like 
         * this
         */
    }
}
